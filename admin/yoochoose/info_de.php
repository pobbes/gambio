<h2>YOOCHOOSE Empfehlungen für Gambio Online-Shops</h2>
<h3>Steigern Sie Ihren Umsatz</h3>
<p class='info_content'>
Führende Online-Shops haben schon seit Langem das Potenzial von professionellen Empfehlungen 
erkannt und systematisch genutzt. Erfahrungen bei anderen Online-Shops zeigen uns, dass - je 
nach Qualität der Empfehlungen - eine <b>Umsatzsteigerung bis zu 20%</b> realistisch ist.

<h3>Bessere Kundenbindung</h3>
<p class='info_content'>
Käufer erwarten gute Empfehlungen in Ihrem Shop. Sie unterstützen den Käufer damit bei dem 
Auffinden des geeigneten Produktes und der Kaufentscheidung. Im Ergänzung zu einer Suche zeigen 
Empfehlungen auch Artikel, <b>von denen der Käufer noch nicht wusste</b>, dass sie im Shop erhältlich sind. 
Mit einem guten Empfehlungsdienst verbessern sie die Usability und Professionalität des Online 
Shops für Ihre Kunden.
<p class='info_content'>
Machen Sie YOOCHOOSE Empfehlungen zu Ihrem besten und erfolgreichsten Vertriebsmitarbeiter im Online-Shop.

<h3>Neue und verbesserte Funktionen</h3>
<p class='info_content'>
<b>Verbesserte "Kunden kauften auch..."-Funktion</b>: Die vorhandene Routine zu "Kunden, welche diesen Artikel bestellten, haben auch folgende Artikel gekauft" wird durch eine deutlich verbesserte Funktion ersetzt, die alle echten Kaufereignisse berücksichtigt.
<p class='info_content'>
<b>"Kunden, die sich für dieses  Produkt interessierten, haben folgende Produkte gekauft...":</b> Diese Funktion ist auch für kleine Shops besonders interessant, da die wesentlich häufigeren Klick-Ereignisse benutzt werden, um Produktempfehlungen zu erstellen. Sie erscheint bei Ihnen in einer zusätzlichen Box im Layout.
<p class='info_content'>
<b>"Kunden, die sich für dieses Produkt interessierten, kauften unmittelbar danach folgende Produkte"</b>: Diese beliebte Empfehlungsart kombiniert die zeitliche Abfolge der Klick- und Kaufereignisse, um Ihre Kunden ideal bei der Entscheidung für das Produktes zu unterstützen. Auch diese Funktion erscheint bei Ihnen in einer zusätzlichen Box.
<p class='info_content'>
<b>Empfehlungen bei der Administration der "Cross-Selling"-Verknüpfung</b>: Der neue "Empfehlungen!"-Button bei der Einrichtung von Cross-Selling Verknüpfungen im Shop-Backend kann Ihnen viel Arbeit ersparen. Sie müssen nicht mehr aufwändig alle zu verknüpfenden Produkte suchen, sondern bekommen zusammen gekaufte Produkte automatisch zur Verknüpfung vorgeschlagen.
